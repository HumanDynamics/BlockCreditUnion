This is a thought-piece, a rough sketch of a set of receipts among participants in an UMA transaction.  It is not complete and likely not right, either.  <br>

The transactions are the items with faux hash names - qwerty[1, 2, 3, 4]. <br>

The basic library is "Library".  <br>

Stock forms are created with the "Agree_[*]" provisions.  A combination of Library and an "Agree_*" gives a form.  (This is not the only way to organize parameterization of the Library.) 

